def import_module_safe(module_name: str, fallback_module: Optional[str] = None) -> Optional[Any]:
    """
    Safe module import with fallback
    
    Args:
        module_name: Primary module to import
        fallback_module: Fallback module if primary fails
        
    Returns:
        Imported module or None if both fail
    """
    import_manager = get_standard_import_manager()
    
    # Try primary module
    module = import_manager.import_module(module_name)
    if module:
        return module
    
    # Try fallback if provided
    if fallback_module:
        logger.info(f"Primary import failed for {module_name}, trying fallback {fallback_module}")
        return import_manager.import_module(fallback_module)
    
    return None

