@dataclass
class ImportPath:
    """Import Path Configuration"""
    name: str
    path: str
    priority: int = 0
    enabled: bool = True
    description: str = ""
    
    def __post_init__(self):
        # Normalize path to absolute
        self.path = str(Path(self.path).resolve())
    
    @property
    def exists(self) -> bool:
        return Path(self.path).exists()

