@contextmanager
def import_context(additional_paths: Optional[List[str]] = None):
    """
    Context manager for temporary import path modifications
    
    Usage:
        with import_context(['/custom/path']):
            # Import modules that require custom paths
            import custom_module
    """
    import_manager = get_standard_import_manager()
    added_paths = []
    
    try:
        # Add additional paths temporarily
        if additional_paths:
            for i, path in enumerate(additional_paths):
                path_name = f"temp_path_{i}"
                if import_manager.add_import_path(path_name, path, priority=-1):
                    added_paths.append(path_name)
        
        yield import_manager
        
    finally:
        # Remove temporary paths
        for path_name in added_paths:
            import_manager.remove_import_path(path_name)

