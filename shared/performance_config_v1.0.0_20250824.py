import os
import logging
from typing import Dict, Any, Optional
from dataclasses import dataclass
from enum import Enum

logger = logging.getLogger(__name__)

class PerformanceProfile(Enum):
    """Performance Profile Definitionen für verschiedene Umgebungen"""
    DEVELOPMENT = "development"
    PRODUCTION = "production"
    HIGH_PERFORMANCE = "high_performance"
    RESOURCE_CONSTRAINED = "resource_constrained"

@dataclass
class SleepIntervals:
    """Konfigurierbare Sleep-Intervalle für verschiedene Operationen"""
    # Monitoring Intervals (Reduziert von 0.1s auf intelligente Werte)
    monitoring_loop: float = 5.0          # Standard: 5s (war 0.1s = 50x Verbesserung)
    health_check: float = 30.0           # Health Check: 30s 
    heartbeat: float = 30.0              # Service Heartbeat: 30s
    
    # Event Processing
    event_processing: float = 0.5        # Event Processing: 500ms (war 100ms = 5x)
    event_retry: float = 1.0             # Event Retry: 1s
    
    # Data Collection Intervals
    market_data_collection: float = 300.0    # Market Data: 5min (war verschiedene Werte)
    portfolio_sync: float = 60.0             # Portfolio Sync: 1min
    prediction_update: float = 1800.0        # Predictions: 30min
    
    # API Rate Limiting (Standardisiert)
    alpha_vantage_delay: float = 12.0        # Alpha Vantage: 12s (Rate Limit)
    iex_cloud_delay: float = 1.0             # IEX Cloud: 1s (optimiert von 5s)
    twelve_data_delay: float = 1.0           # Twelve Data: 1s (optimiert von 2s) 
    eod_historical_delay: float = 1.0        # EOD Historical: 1s (optimiert von 0.5s/3s)
    finnhub_delay: float = 1.0               # Finnhub: 1s (optimiert von 3s)
    
    # Error Recovery
    error_backoff_base: float = 5.0          # Base backoff: 5s
    error_backoff_max: float = 300.0         # Max backoff: 5min
    
    # Background Tasks
    cleanup_interval: float = 3600.0         # Cleanup: 1h
    backup_interval: float = 14400.0         # Backup: 4h

