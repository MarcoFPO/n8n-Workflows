class PerformanceConfigManager:
    """
    Zentrale Performance-Konfiguration Manager
    
    Verwaltet alle Performance-relevanten Einstellungen:
    - Environment-basierte Konfiguration
    - Profile-spezifische Optimierungen  
    - Runtime-Performance Anpassungen
    """
    
    def __init__(self, profile: Optional[PerformanceProfile] = None):
        """
        Initialisiert Performance-Konfiguration
        
        Args:
            profile: Performance Profil (auto-detect wenn None)
        """
        self.profile = profile or self._detect_profile()
        self._load_configuration()
        logger.info(f"Performance-Konfiguration geladen: {self.profile.value}")
    
    def _detect_profile(self) -> PerformanceProfile:
        """Auto-Detection des Performance-Profils basierend auf Environment"""
        env = os.getenv('AKTIENANALYSE_ENV', 'development').lower()
        
        profile_mapping = {
            'development': PerformanceProfile.DEVELOPMENT,
            'testing': PerformanceProfile.DEVELOPMENT, 
            'production': PerformanceProfile.PRODUCTION,
            'high_performance': PerformanceProfile.HIGH_PERFORMANCE,
            'resource_constrained': PerformanceProfile.RESOURCE_CONSTRAINED
        }
        
        return profile_mapping.get(env, PerformanceProfile.DEVELOPMENT)
    
    def _load_configuration(self) -> None:
        """Lädt Profile-spezifische Konfiguration"""
        if self.profile == PerformanceProfile.DEVELOPMENT:
            self._load_development_config()
        elif self.profile == PerformanceProfile.PRODUCTION:
            self._load_production_config()
        elif self.profile == PerformanceProfile.HIGH_PERFORMANCE:
            self._load_high_performance_config()
        elif self.profile == PerformanceProfile.RESOURCE_CONSTRAINED:
            self._load_resource_constrained_config()
            
    def _load_development_config(self) -> None:
        """Development Profile: Ausgewogen zwischen Performance und Debugging"""
        self.sleep_intervals = SleepIntervals(
            monitoring_loop=float(os.getenv('MONITORING_LOOP_INTERVAL', '5.0')),
            health_check=float(os.getenv('HEALTH_CHECK_INTERVAL', '30.0')),
            event_processing=float(os.getenv('EVENT_PROCESSING_INTERVAL', '0.5')),
            market_data_collection=float(os.getenv('MARKET_DATA_INTERVAL', '300.0'))
        )
        
        self.http_config = HttpClientConfig(
            max_connections=int(os.getenv('HTTP_MAX_CONNECTIONS', '50')),
            connection_timeout=float(os.getenv('HTTP_CONNECTION_TIMEOUT', '10.0'))
        )
        
        self.rate_limits = RateLimitConfig()
        self.metrics = PerformanceMetrics()
        
    def _load_production_config(self) -> None:
        """Production Profile: Optimiert für Stabilität und Performance"""
        self.sleep_intervals = SleepIntervals(
            monitoring_loop=float(os.getenv('MONITORING_LOOP_INTERVAL', '10.0')),    # Weniger häufig
            health_check=float(os.getenv('HEALTH_CHECK_INTERVAL', '60.0')),          # 1min Health Check
            event_processing=float(os.getenv('EVENT_PROCESSING_INTERVAL', '0.2')),   # Schnellere Events
            market_data_collection=float(os.getenv('MARKET_DATA_INTERVAL', '600.0')) # 10min Market Data
        )
        
        self.http_config = HttpClientConfig(
            max_connections=int(os.getenv('HTTP_MAX_CONNECTIONS', '100')),
            connection_timeout=float(os.getenv('HTTP_CONNECTION_TIMEOUT', '5.0'))
        )
        
        self.rate_limits = RateLimitConfig(
            event_publishing_rps=int(os.getenv('EVENT_PUBLISHING_RPS', '2000'))
        )
        
        self.metrics = PerformanceMetrics(
            target_event_processing_ms=int(os.getenv('TARGET_EVENT_PROCESSING_MS', '30'))
        )
        
    def _load_high_performance_config(self) -> None:
        """High Performance Profile: Maximum Performance"""
        self.sleep_intervals = SleepIntervals(
            monitoring_loop=float(os.getenv('MONITORING_LOOP_INTERVAL', '2.0')),     # Sehr schnell
            health_check=float(os.getenv('HEALTH_CHECK_INTERVAL', '15.0')),          # Häufige Health Checks
            event_processing=float(os.getenv('EVENT_PROCESSING_INTERVAL', '0.1')),   # Ultra-schnell
            market_data_collection=float(os.getenv('MARKET_DATA_INTERVAL', '60.0'))  # 1min Market Data
        )
        
        self.http_config = HttpClientConfig(
            max_connections=int(os.getenv('HTTP_MAX_CONNECTIONS', '200')),           # Mehr Connections
            connection_timeout=float(os.getenv('HTTP_CONNECTION_TIMEOUT', '3.0')),   # Schnelle Timeouts
            keepalive_max_requests=int(os.getenv('HTTP_KEEPALIVE_REQUESTS', '200'))  # Mehr Keep-Alive
        )
        
        self.rate_limits = RateLimitConfig(
            event_publishing_rps=int(os.getenv('EVENT_PUBLISHING_RPS', '5000')),     # Hohe Throughput
            database_query_rps=int(os.getenv('DATABASE_QUERY_RPS', '2000'))
        )
        
        self.metrics = PerformanceMetrics(
            target_event_processing_ms=int(os.getenv('TARGET_EVENT_PROCESSING_MS', '20')), # Ultra-schnell
            target_events_per_second=int(os.getenv('TARGET_EVENTS_PER_SECOND', '5000'))
        )
    
    def _load_resource_constrained_config(self) -> None:
        """Resource Constrained Profile: Minimaler Ressourcenverbrauch"""
        self.sleep_intervals = SleepIntervals(
            monitoring_loop=float(os.getenv('MONITORING_LOOP_INTERVAL', '30.0')),    # Sehr selten
            health_check=float(os.getenv('HEALTH_CHECK_INTERVAL', '120.0')),         # 2min Health Check  
            event_processing=float(os.getenv('EVENT_PROCESSING_INTERVAL', '2.0')),   # Langsamere Events
            market_data_collection=float(os.getenv('MARKET_DATA_INTERVAL', '1800.0')) # 30min Market Data
        )
        
        self.http_config = HttpClientConfig(
            max_connections=int(os.getenv('HTTP_MAX_CONNECTIONS', '20')),            # Weniger Connections
            connection_timeout=float(os.getenv('HTTP_CONNECTION_TIMEOUT', '15.0')),  # Längere Timeouts
            max_connections_per_host=int(os.getenv('HTTP_MAX_CONN_PER_HOST', '5'))   # Begrenzte Connections
        )
        
        self.rate_limits = RateLimitConfig(
            event_publishing_rps=int(os.getenv('EVENT_PUBLISHING_RPS', '100')),      # Niedrige Throughput
            database_query_rps=int(os.getenv('DATABASE_QUERY_RPS', '50'))
        )
        
        self.metrics = PerformanceMetrics(
            target_memory_mb=int(os.getenv('TARGET_MEMORY_MB', '64')),               # Weniger Memory
            target_cpu_percent=float(os.getenv('TARGET_CPU_PERCENT', '15.0'))       # Weniger CPU
        )
    
    def get_sleep_interval(self, operation: str) -> float:
        """
        Holt optimiertes Sleep-Interval für spezifische Operation
        
        Args:
            operation: Name der Operation (z.B. 'monitoring_loop', 'health_check')
            
        Returns:
            Optimiertes Sleep-Interval in Sekunden
        """
        return getattr(self.sleep_intervals, operation, 1.0)
    
    def get_api_delay(self, provider: str) -> float:
        """
        Holt API-spezifisches Rate-Limiting Delay
        
        Args:
            provider: API Provider Name (z.B. 'alpha_vantage', 'iex_cloud')
            
        Returns:
            Delay in Sekunden zwischen API Calls
        """
        delay_mapping = {
            'alpha_vantage': self.sleep_intervals.alpha_vantage_delay,
            'iex_cloud': self.sleep_intervals.iex_cloud_delay,
            'twelve_data': self.sleep_intervals.twelve_data_delay,
            'eod_historical': self.sleep_intervals.eod_historical_delay,
            'finnhub': self.sleep_intervals.finnhub_delay
        }
        return delay_mapping.get(provider, 1.0)
    
    def get_http_config(self) -> HttpClientConfig:
        """Returns HTTP Client Konfiguration"""
        return self.http_config
    
    def get_rate_limit_config(self) -> RateLimitConfig:
        """Returns Rate Limiting Konfiguration"""
        return self.rate_limits
    
    def get_performance_metrics(self) -> PerformanceMetrics:
        """Returns Performance Metriken Konfiguration"""
        return self.metrics
    
    def log_configuration_summary(self) -> None:
        """Loggt zusammenfassende Konfiguration für Debugging"""
        logger.info(f"Performance Profile: {self.profile.value}")
        logger.info(f"Monitoring Loop Interval: {self.sleep_intervals.monitoring_loop}s")
        logger.info(f"Event Processing Interval: {self.sleep_intervals.event_processing}s")
        logger.info(f"HTTP Max Connections: {self.http_config.max_connections}")
        logger.info(f"Target Event Processing: {self.metrics.target_event_processing_ms}ms")

# Globale Performance-Konfiguration Instanz
_performance_config: Optional[PerformanceConfigManager] = None

def get_performance_config() -> PerformanceConfigManager:
    """
    Singleton-Pattern für globale Performance-Konfiguration
    
    Returns:
        Globale PerformanceConfigManager Instanz
    """
    global _performance_config
    if _performance_config is None:
        _performance_config = PerformanceConfigManager()
        _performance_config.log_configuration_summary()
    return _performance_config

def optimize_sleep_interval(operation: str, default: float = 1.0) -> float:
    """
    Convenience-Funktion für optimierte Sleep-Intervalle
    
    Args:
        operation: Operation Name
        default: Default-Wert wenn nicht konfiguriert
        
    Returns:
        Optimiertes Sleep-Interval
    """
    config = get_performance_config()
    return config.get_sleep_interval(operation) if hasattr(config.sleep_intervals, operation) else default

def get_api_rate_limit_delay(provider: str) -> float:
    """
    Convenience-Funktion für API Rate-Limiting Delays
    
    Args:
        provider: API Provider Name
        
    Returns:
        Rate-Limiting Delay in Sekunden
    """
    config = get_performance_config()
    return config.get_api_delay(provider)

