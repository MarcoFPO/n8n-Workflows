@asynccontextmanager
async def event_bus_context(service_name: str = "default"):
    """Context manager for event bus lifecycle"""
    event_bus = SharedEventBusManager(service_name=service_name)
    
    try:
        await event_bus.initialize()
        yield event_bus
    finally:
        await event_bus.shutdown()

