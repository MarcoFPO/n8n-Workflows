class UnifiedEventPublisher(IEventPublisher):
    """
    Unified Event Publisher - Single Responsibility
    Consolidates publishing logic from all previous implementations
    """
    
    def __init__(self, 
                 transport: IEventTransport,
                 router: IEventRouter,
                 subscription_manager: UnifiedSubscriptionManager):
        self.transport = transport
        self.router = router
        self.subscription_manager = subscription_manager
        self.publish_queue = asyncio.Queue(maxsize=10000)
        self.stats = {
            'events_published': 0,
            'events_failed': 0,
            'batch_publications': 0,
            'queue_size': 0
        }
        self.running = False
        self.publisher_task: Optional[asyncio.Task] = None
    
    async def start(self):
        """Start background publisher"""
        self.running = True
        self.publisher_task = asyncio.create_task(self._process_publish_queue())
    
    async def stop(self):
        """Stop background publisher"""
        self.running = False
        if self.publisher_task:
            self.publisher_task.cancel()
            try:
                await self.publisher_task
            except asyncio.CancelledError:
                pass
    
    async def publish(self, event: Event) -> bool:
        """Publish single event"""
        try:
            # Route event to determine targets
            target_services = await self.router.route_event(event)
            event.metadata.target_services = target_services
            
            # Publish via transport
            success = await self.transport.publish(event)
            
            # Dispatch to local subscribers
            await self.subscription_manager.dispatch_event(event)
            
            if success:
                self.stats['events_published'] += 1
            else:
                self.stats['events_failed'] += 1
            
            # Performance monitoring integration
            perf_monitor = get_performance_monitor()
            perf_monitor.record_event_published(event.event_type, success)
            
            return success
            
        except Exception as e:
            logger.error(f"Failed to publish event {event.metadata.event_id}: {e}")
            self.stats['events_failed'] += 1
            return False
    
    async def publish_batch(self, events: List[Event]) -> List[bool]:
        """Publish multiple events efficiently"""
        results = []
        
        for event in events:
            result = await self.publish(event)
            results.append(result)
        
        self.stats['batch_publications'] += 1
        return results
    
    async def publish_async(self, event: Event) -> None:
        """Publish event asynchronously via queue"""
        try:
            await self.publish_queue.put(event)
            self.stats['queue_size'] = self.publish_queue.qsize()
        except asyncio.QueueFull:
            logger.warning("Publish queue full, dropping event")
            self.stats['events_failed'] += 1
    
    async def _process_publish_queue(self):
        """Background task to process publish queue"""
        while self.running:
            try:
                event = await asyncio.wait_for(self.publish_queue.get(), timeout=1.0)
                await self.publish(event)
                self.publish_queue.task_done()
                self.stats['queue_size'] = self.publish_queue.qsize()
                
            except asyncio.TimeoutError:
                continue
            except Exception as e:
                logger.error(f"Publish queue processing error: {e}")

