@dataclass
class Subscription:
    """Event Subscription Information"""
    subscription_id: str
    event_type: str
    handler: Callable[[Event], Any]
    service_name: str
    created_at: datetime = field(default_factory=datetime.now)
    active: bool = True
    call_count: int = 0
    last_called: Optional[datetime] = None

class UnifiedSubscriptionManager(IEventSubscriber):
    """
    Unified Subscription Manager - Single Responsibility 
    Consolidates subscription logic from all previous implementations
    """
    
    def __init__(self):
        self.subscriptions: Dict[str, Subscription] = {}
        self.event_handlers: Dict[str, List[str]] = {}  # event_type -> subscription_ids
        self.stats = {
            'total_subscriptions': 0,
            'active_subscriptions': 0,
            'events_dispatched': 0,
            'handler_errors': 0
        }
    
    async def subscribe(self, event_type: str, handler: Callable[[Event], Any], 
                       service_name: str = "unknown") -> str:
        """Subscribe to event type"""
        subscription_id = str(uuid.uuid4())
        
        subscription = Subscription(
            subscription_id=subscription_id,
            event_type=event_type,
            handler=handler,
            service_name=service_name
        )
        
        self.subscriptions[subscription_id] = subscription
        
        # Add to event type mapping
        if event_type not in self.event_handlers:
            self.event_handlers[event_type] = []
        self.event_handlers[event_type].append(subscription_id)
        
        self._update_stats()
        logger.debug(f"Added subscription {subscription_id} for {event_type}")
        return subscription_id
    
    async def unsubscribe(self, subscription_id: str) -> bool:
        """Unsubscribe from events"""
        if subscription_id not in self.subscriptions:
            return False
        
        subscription = self.subscriptions[subscription_id]
        
        # Remove from event type mapping
        if subscription.event_type in self.event_handlers:
            if subscription_id in self.event_handlers[subscription.event_type]:
                self.event_handlers[subscription.event_type].remove(subscription_id)
            
            # Clean up empty event type mappings
            if not self.event_handlers[subscription.event_type]:
                del self.event_handlers[subscription.event_type]
        
        # Remove subscription
        del self.subscriptions[subscription_id]
        
        self._update_stats()
        logger.debug(f"Removed subscription {subscription_id}")
        return True
    
    def get_subscriptions(self) -> Dict[str, List[str]]:
        """Get all active subscriptions by event type"""
        return {
            event_type: [sub_id for sub_id in sub_ids 
                        if sub_id in self.subscriptions and self.subscriptions[sub_id].active]
            for event_type, sub_ids in self.event_handlers.items()
        }
    
    async def dispatch_event(self, event: Event) -> Dict[str, bool]:
        """Dispatch event to all subscribers"""
        results = {}
        subscription_ids = self.event_handlers.get(event.event_type, [])
        
        for subscription_id in subscription_ids:
            if subscription_id not in self.subscriptions:
                continue
            
            subscription = self.subscriptions[subscription_id]
            if not subscription.active:
                continue
            
            try:
                # Call handler (sync or async)
                if asyncio.iscoroutinefunction(subscription.handler):
                    await subscription.handler(event)
                else:
                    subscription.handler(event)
                
                # Update subscription stats
                subscription.call_count += 1
                subscription.last_called = datetime.now()
                
                results[subscription_id] = True
                self.stats['events_dispatched'] += 1
                
            except Exception as e:
                logger.error(f"Handler error for {subscription_id}: {e}")
                results[subscription_id] = False
                self.stats['handler_errors'] += 1
        
        return results
    
    def _update_stats(self):
        """Update subscription statistics"""
        self.stats['total_subscriptions'] = len(self.subscriptions)
        self.stats['active_subscriptions'] = len([s for s in self.subscriptions.values() if s.active])

