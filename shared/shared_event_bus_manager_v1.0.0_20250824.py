class EventBusConnector:
    """Backward-compatible interface for existing code"""
    
    def __init__(self, service_name: str):
        self.service_name = service_name
        self._event_bus = SharedEventBusManager(service_name=service_name)
    
    async def connect(self) -> bool:
        return await self._event_bus.initialize()
    
    async def disconnect(self):
        await self._event_bus.shutdown()
    
    def is_connected(self) -> bool:
        return self._event_bus.initialized
    
    async def publish(self, event_data: Union[Event, Dict[str, Any]]) -> bool:
        if isinstance(event_data, Event):
            return await self._event_bus.publish_event(event_data)
        else:
            # Legacy dict-based event
            return await self._event_bus.publish(
                event_data.get('event_type', 'unknown'),
                event_data.get('data', {})
            )
    
    async def subscribe(self, event_type: str, handler: Callable) -> str:
        return await self._event_bus.subscribe(event_type, handler)
    
    async def subscribe_realtime(self, event_type: str, handler: Callable) -> bool:
        subscription_id = await self.subscribe(event_type, handler)
        return bool(subscription_id)

