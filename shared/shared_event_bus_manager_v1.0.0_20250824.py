@dataclass
class RoutingRule:
    """Event Routing Rule Definition"""
    rule_id: str
    event_pattern: str
    target_services: List[str]
    conditions: Dict[str, Any] = field(default_factory=dict)
    priority: int = 0
    enabled: bool = True

class UnifiedEventRouter(IEventRouter):
    """
    Unified Event Router - Single Responsibility
    Consolidates routing logic from all previous implementations
    """
    
    def __init__(self):
        self.routing_rules: Dict[str, RoutingRule] = {}
        self.stats = {
            'rules_count': 0,
            'events_routed': 0,
            'routing_errors': 0
        }
        self._setup_default_rules()
    
    def _setup_default_rules(self):
        """Setup default routing rules for common patterns"""
        # Default rule: route all events to local subscribers
        default_rule = RoutingRule(
            rule_id="default_local",
            event_pattern="*",
            target_services=["local_subscribers"],
            priority=0
        )
        self.add_routing_rule_obj(default_rule)
        
        # High priority events to monitoring
        monitoring_rule = RoutingRule(
            rule_id="high_priority_monitoring",
            event_pattern="*",
            target_services=["monitoring"],
            conditions={"priority": EventPriority.HIGH.value},
            priority=10
        )
        self.add_routing_rule_obj(monitoring_rule)
    
    async def route_event(self, event: Event) -> List[str]:
        """Route event to appropriate target services"""
        try:
            target_services = set()
            
            # Apply routing rules in priority order
            sorted_rules = sorted(self.routing_rules.values(), 
                                key=lambda r: r.priority, reverse=True)
            
            for rule in sorted_rules:
                if not rule.enabled:
                    continue
                
                if self._matches_pattern(event.event_type, rule.event_pattern):
                    if self._matches_conditions(event, rule.conditions):
                        target_services.update(rule.target_services)
            
            # Add explicitly targeted services
            if event.metadata.target_services:
                target_services.update(event.metadata.target_services)
            
            self.stats['events_routed'] += 1
            return list(target_services)
            
        except Exception as e:
            logger.error(f"Event routing error: {e}")
            self.stats['routing_errors'] += 1
            return ["local_subscribers"]  # Fallback
    
    def add_routing_rule(self, pattern: str, target_services: List[str]) -> str:
        """Add simple routing rule"""
        rule_id = str(uuid.uuid4())
        rule = RoutingRule(
            rule_id=rule_id,
            event_pattern=pattern,
            target_services=target_services
        )
        return self.add_routing_rule_obj(rule)
    
    def add_routing_rule_obj(self, rule: RoutingRule) -> str:
        """Add routing rule object"""
        self.routing_rules[rule.rule_id] = rule
        self.stats['rules_count'] = len(self.routing_rules)
        logger.debug(f"Added routing rule: {rule.rule_id}")
        return rule.rule_id
    
    def remove_routing_rule(self, rule_id: str) -> bool:
        """Remove routing rule"""
        if rule_id in self.routing_rules:
            del self.routing_rules[rule_id]
            self.stats['rules_count'] = len(self.routing_rules)
            logger.debug(f"Removed routing rule: {rule_id}")
            return True
        return False
    
    def _matches_pattern(self, event_type: str, pattern: str) -> bool:
        """Check if event type matches pattern"""
        if pattern == "*":
            return True
        if pattern.endswith("*"):
            return event_type.startswith(pattern[:-1])
        return event_type == pattern
    
    def _matches_conditions(self, event: Event, conditions: Dict[str, Any]) -> bool:
        """Check if event matches additional conditions"""
        if not conditions:
            return True
        
        for key, expected_value in conditions.items():
            if key == "priority":
                if event.metadata.priority.value < expected_value:
                    return False
            elif key == "source_service":
                if event.metadata.source_service != expected_value:
                    return False
            elif key in event.data:
                if event.data[key] != expected_value:
                    return False
            else:
                return False
        
        return True

