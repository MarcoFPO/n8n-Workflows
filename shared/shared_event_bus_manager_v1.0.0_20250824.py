class InMemoryTransport(IEventTransport):
    """
    High-Performance In-Memory Event Transport
    Optimized for single-process event handling with minimal latency
    """
    
    def __init__(self):
        self.subscribers: Dict[str, List[Tuple[str, Callable[[Event], Any]]]] = {}
        self.event_queue = asyncio.Queue(maxsize=10000)
        self.stats = {
            'events_published': 0,
            'events_delivered': 0,
            'events_failed': 0,
            'active_subscriptions': 0,
            'queue_size': 0
        }
        self.running = False
        self.processor_task: Optional[asyncio.Task] = None
    
    async def initialize(self) -> bool:
        """Initialize in-memory transport"""
        try:
            self.running = True
            self.processor_task = asyncio.create_task(self._process_events())
            logger.info("In-Memory transport initialized")
            return True
        except Exception as e:
            logger.error(f"Failed to initialize in-memory transport: {e}")
            return False
    
    async def shutdown(self) -> None:
        """Shutdown in-memory transport"""
        self.running = False
        if self.processor_task:
            self.processor_task.cancel()
            try:
                await self.processor_task
            except asyncio.CancelledError:
                pass
        logger.info("In-Memory transport shutdown")
    
    async def publish(self, event: Event) -> bool:
        """Publish event to in-memory queue"""
        try:
            await self.event_queue.put(event)
            self.stats['events_published'] += 1
            self.stats['queue_size'] = self.event_queue.qsize()
            return True
        except asyncio.QueueFull:
            logger.warning("Event queue full, dropping event")
            self.stats['events_failed'] += 1
            return False
        except Exception as e:
            logger.error(f"Failed to publish event: {e}")
            self.stats['events_failed'] += 1
            return False
    
    async def subscribe(self, event_type: str, handler: Callable[[Event], Any]) -> str:
        """Subscribe to events"""
        subscription_id = str(uuid.uuid4())
        
        if event_type not in self.subscribers:
            self.subscribers[event_type] = []
        
        self.subscribers[event_type].append((subscription_id, handler))
        self.stats['active_subscriptions'] = sum(len(subs) for subs in self.subscribers.values())
        
        logger.debug(f"Subscribed to {event_type} with ID {subscription_id}")
        return subscription_id
    
    async def unsubscribe(self, subscription_id: str) -> bool:
        """Unsubscribe from events"""
        for event_type, subscribers in self.subscribers.items():
            for i, (sub_id, _) in enumerate(subscribers):
                if sub_id == subscription_id:
                    subscribers.pop(i)
                    self.stats['active_subscriptions'] = sum(len(subs) for subs in self.subscribers.values())
                    logger.debug(f"Unsubscribed {subscription_id} from {event_type}")
                    return True
        return False
    
    def get_stats(self) -> Dict[str, Any]:
        """Get transport statistics"""
        return self.stats.copy()
    
    async def _process_events(self):
        """Background event processor"""
        while self.running:
            try:
                # Get event from queue with timeout
                event = await asyncio.wait_for(self.event_queue.get(), timeout=1.0)
                await self._deliver_event(event)
                self.event_queue.task_done()
                self.stats['queue_size'] = self.event_queue.qsize()
                
            except asyncio.TimeoutError:
                continue
            except Exception as e:
                logger.error(f"Event processing error: {e}")
    
    async def _deliver_event(self, event: Event):
        """Deliver event to subscribers"""
        subscribers = self.subscribers.get(event.event_type, [])
        
        for subscription_id, handler in subscribers:
            try:
                if asyncio.iscoroutinefunction(handler):
                    await handler(event)
                else:
                    handler(event)
                
                self.stats['events_delivered'] += 1
                
            except Exception as e:
                logger.error(f"Event handler error for {subscription_id}: {e}")
                self.stats['events_failed'] += 1

