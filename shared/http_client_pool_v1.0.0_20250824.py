async def cleanup_http_pools():
    """Cleanup-Funktion für alle HTTP Pools (für graceful shutdown)"""
    global _http_pool_manager
    if _http_pool_manager:
        await _http_pool_manager.close_all()
        _http_pool_manager = None
        logger.info("HTTP Client Pools cleanup abgeschlossen")

