@dataclass
class RequestStats:
    """HTTP Request Statistiken für Performance-Monitoring"""
    total_requests: int = 0
    successful_requests: int = 0
    failed_requests: int = 0
    total_response_time: float = 0.0
    average_response_time: float = 0.0
    connection_reuses: int = 0
    new_connections: int = 0
    
    def update_stats(self, response_time: float, success: bool, connection_reused: bool):
        """Aktualisiert Request-Statistiken"""
        self.total_requests += 1
        if success:
            self.successful_requests += 1
        else:
            self.failed_requests += 1
            
        self.total_response_time += response_time
        self.average_response_time = self.total_response_time / self.total_requests
        
        if connection_reused:
            self.connection_reuses += 1
        else:
            self.new_connections += 1

