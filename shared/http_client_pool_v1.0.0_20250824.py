class CircuitBreaker:
    """
    Circuit Breaker Pattern für HTTP Clients
    Verhindert Cascade Failures bei API-Ausfällen
    """
    
    def __init__(self, failure_threshold: int = 5, reset_timeout: float = 60.0):
        self.failure_threshold = failure_threshold
        self.reset_timeout = reset_timeout
        self.failure_count = 0
        self.last_failure_time = None
        self.state = 'closed'  # closed, open, half-open
        
    def can_execute(self) -> bool:
        """Prüft ob Request ausgeführt werden kann"""
        if self.state == 'closed':
            return True
        elif self.state == 'open':
            if time.time() - self.last_failure_time >= self.reset_timeout:
                self.state = 'half-open'
                return True
            return False
        elif self.state == 'half-open':
            return True
        return False
    
    def record_success(self):
        """Registriert erfolgreichen Request"""
        self.failure_count = 0
        self.state = 'closed'
    
    def record_failure(self):
        """Registriert fehlgeschlagenen Request"""
        self.failure_count += 1
        self.last_failure_time = time.time()
        
        if self.failure_count >= self.failure_threshold:
            self.state = 'open'
            logger.warning(f"Circuit Breaker OPEN - {self.failure_count} failures")

class SharedHttpClient:
    """
    Shared HTTP Client mit Connection Pooling und Performance-Optimierungen
    """
    
    def __init__(self, config: HttpClientPoolConfig, http_config: HttpClientConfig):
        self.config = config
        self.http_config = http_config
        self.session: Optional[aiohttp.ClientSession] = None
        self.stats = RequestStats()
        self.circuit_breaker = CircuitBreaker(
            config.circuit_breaker_threshold,
            config.circuit_breaker_timeout
        )
        self._lock = asyncio.Lock()
        self._closed = False
        
    async def __aenter__(self):
        await self.initialize()
        return self
    
    async def __aexit__(self, exc_type, exc_val, exc_tb):
        await self.close()
    
    async def initialize(self):
        """Initialisiert HTTP Session mit optimierter Konfiguration"""
        if self.session is not None:
            return
            
        # TCP Connector mit Connection Pooling
        connector = aiohttp.TCPConnector(
            limit=self.http_config.max_connections,
            limit_per_host=self.http_config.max_connections_per_host,
            keepalive_timeout=self.http_config.keepalive_timeout,
            enable_cleanup_closed=True,
            use_dns_cache=True,
            ttl_dns_cache=300,  # 5 min DNS cache
            family=0  # Allow both IPv4 and IPv6
        )
        
        # Timeout Konfiguration
        timeout = aiohttp.ClientTimeout(
            total=self.http_config.total_timeout,
            connect=self.http_config.connection_timeout,
            sock_read=self.http_config.read_timeout
        )
        
        # Session mit Performance-Optimierungen
        self.session = aiohttp.ClientSession(
            connector=connector,
            timeout=timeout,
            headers=self.config.headers,
            raise_for_status=False,  # Handle errors manually
            read_bufsize=2**16,  # 64KB read buffer
            conn_timeout=self.http_config.connection_timeout,
            read_timeout=self.http_config.read_timeout
        )
        
        logger.info(f"HTTP Client Pool '{self.config.name}' initialisiert")
        
    async def request(self, method: str, url: str, **kwargs) -> aiohttp.ClientResponse:
        """
        Optimierter HTTP Request mit Performance-Tracking
        
        Args:
            method: HTTP Method (GET, POST, etc.)
            url: Request URL
            **kwargs: Additional request parameters
            
        Returns:
            aiohttp.ClientResponse
            
        Raises:
            aiohttp.ClientError: Bei HTTP Fehlern
            RuntimeError: Bei Circuit Breaker OPEN
        """
        if not self.circuit_breaker.can_execute():
            raise RuntimeError(f"Circuit Breaker OPEN for {self.config.name}")
        
        await self.initialize()
        
        start_time = time.time()
        connection_reused = True
        
        try:
            # Full URL construction
            if self.config.base_url and not url.startswith('http'):
                full_url = f"{self.config.base_url.rstrip('/')}/{url.lstrip('/')}"
            else:
                full_url = url
            
            # Execute request
            async with self.session.request(method, full_url, **kwargs) as response:
                response_time = time.time() - start_time
                
                # Determine if connection was reused (heuristic)
                connection_reused = response_time < 0.1
                
                # Update statistics
                success = response.status < 400
                self.stats.update_stats(response_time, success, connection_reused)
                
                # Circuit Breaker handling
                if success:
                    self.circuit_breaker.record_success()
                else:
                    self.circuit_breaker.record_failure()
                    logger.warning(f"HTTP {response.status} for {method} {full_url}")
                
                return response
                
        except Exception as e:
            response_time = time.time() - start_time
            self.stats.update_stats(response_time, False, False)
            self.circuit_breaker.record_failure()
            
            logger.error(f"HTTP Request failed: {method} {url} - {str(e)}")
            raise
    
    async def get(self, url: str, **kwargs) -> aiohttp.ClientResponse:
        """GET Request"""
        return await self.request('GET', url, **kwargs)
    
    async def post(self, url: str, **kwargs) -> aiohttp.ClientResponse:
        """POST Request"""
        return await self.request('POST', url, **kwargs)
    
    async def put(self, url: str, **kwargs) -> aiohttp.ClientResponse:
        """PUT Request"""
        return await self.request('PUT', url, **kwargs)
        
    async def delete(self, url: str, **kwargs) -> aiohttp.ClientResponse:
        """DELETE Request"""
        return await self.request('DELETE', url, **kwargs)
    
    async def get_json(self, url: str, **kwargs) -> Dict[str, Any]:
        """
        GET Request mit automatischer JSON-Dekodierung
        
        Returns:
            Parsed JSON als Dictionary
        """
        response = await self.get(url, **kwargs)
        if response.status >= 400:
            response.raise_for_status()
        return await response.json()
    
    async def post_json(self, url: str, data: Any = None, **kwargs) -> Dict[str, Any]:
        """
        POST Request mit JSON Payload und automatischer JSON-Dekodierung
        
        Args:
            url: Target URL
            data: JSON-serialisierbares Payload
            **kwargs: Additional request parameters
            
        Returns:
            Parsed JSON Response
        """
        if data is not None:
            kwargs['json'] = data
            
        response = await self.post(url, **kwargs)
        if response.status >= 400:
            response.raise_for_status()
        return await response.json()
    
    def get_stats(self) -> Dict[str, Any]:
        """Returns HTTP Client Performance-Statistiken"""
        connection_reuse_rate = (
            self.stats.connection_reuses / max(1, self.stats.total_requests) * 100
        )
        success_rate = (
            self.stats.successful_requests / max(1, self.stats.total_requests) * 100
        )
        
        return {
            'client_name': self.config.name,
            'total_requests': self.stats.total_requests,
            'success_rate_percent': round(success_rate, 2),
            'average_response_time_ms': round(self.stats.average_response_time * 1000, 2),
            'connection_reuse_rate_percent': round(connection_reuse_rate, 2),
            'new_connections': self.stats.new_connections,
            'circuit_breaker_state': self.circuit_breaker.state,
            'failure_count': self.circuit_breaker.failure_count
        }
    
    async def close(self):
        """Schließt HTTP Client Session"""
        if self.session and not self._closed:
            await self.session.close()
            self._closed = True
            logger.info(f"HTTP Client Pool '{self.config.name}' geschlossen")

class HttpClientPoolManager:
    """
    Zentraler Manager für alle HTTP Client Pools
    
    Verwaltet verschiedene Client-Pools für:
    - API-Provider (Alpha Vantage, IEX Cloud, etc.)
    - Interne Services
    - External Services
    """
    
    def __init__(self):
        self._clients: Dict[str, SharedHttpClient] = {}
        self._http_config = get_performance_config().get_http_config()
        self._lock = asyncio.Lock()
        logger.info("HTTP Client Pool Manager initialisiert")
    
    async def get_client(self, name: str, config: Optional[HttpClientPoolConfig] = None) -> SharedHttpClient:
        """
        Holt oder erstellt HTTP Client Pool
        
        Args:
            name: Client Pool Name
            config: Optional Client-Konfiguration
            
        Returns:
            SharedHttpClient Instanz
        """
        if name in self._clients:
            return self._clients[name]
        
        async with self._lock:
            # Double-check pattern
            if name in self._clients:
                return self._clients[name]
            
            # Erstelle neue Client-Instanz
            if config is None:
                config = HttpClientPoolConfig(name=name)
            
            client = SharedHttpClient(config, self._http_config)
            await client.initialize()
            self._clients[name] = client
            
            logger.info(f"HTTP Client Pool '{name}' erstellt")
            return client
    
    async def get_api_client(self, provider: str, base_url: str, headers: Optional[Dict[str, str]] = None) -> SharedHttpClient:
        """
        Holt API-spezifischen HTTP Client Pool
        
        Args:
            provider: API Provider Name (z.B. 'alpha_vantage')
            base_url: Base URL für API
            headers: Optional HTTP Headers
            
        Returns:
            SharedHttpClient für API Provider
        """
        config = HttpClientPoolConfig(
            name=f"api_{provider}",
            base_url=base_url,
            headers=headers or {},
            circuit_breaker_threshold=3  # Niedrigere Schwelle für APIs
        )
        
        return await self.get_client(f"api_{provider}", config)
    
    async def get_internal_client(self, service: str, base_url: str) -> SharedHttpClient:
        """
        Holt internen Service HTTP Client Pool
        
        Args:
            service: Service Name
            base_url: Service Base URL
            
        Returns:
            SharedHttpClient für internen Service
        """
        config = HttpClientPoolConfig(
            name=f"internal_{service}",
            base_url=base_url,
            headers={'User-Agent': 'AktienanalyseOkosystem/1.0'},
            circuit_breaker_threshold=10  # Höhere Schwelle für interne Services
        )
        
        return await self.get_client(f"internal_{service}", config)
    
    def get_all_stats(self) -> Dict[str, Dict[str, Any]]:
        """
        Returns Performance-Statistiken aller HTTP Client Pools
        
        Returns:
            Dictionary mit Statistiken pro Client Pool
        """
        return {
            name: client.get_stats() 
            for name, client in self._clients.items()
        }
    
    async def close_all(self):
        """Schließt alle HTTP Client Pools"""
        close_tasks = [client.close() for client in self._clients.values()]
        if close_tasks:
            await asyncio.gather(*close_tasks, return_exceptions=True)
        self._clients.clear()
        logger.info("Alle HTTP Client Pools geschlossen")
    
    async def health_check(self) -> Dict[str, Any]:
        """
        Health Check aller HTTP Client Pools
        
        Returns:
            Health Status Dictionary
        """
        stats = self.get_all_stats()
        healthy_clients = sum(
            1 for client_stats in stats.values() 
            if client_stats['circuit_breaker_state'] == 'closed'
        )
        
        return {
            'total_clients': len(self._clients),
            'healthy_clients': healthy_clients,
            'unhealthy_clients': len(self._clients) - healthy_clients,
            'total_requests': sum(s['total_requests'] for s in stats.values()),
            'overall_success_rate': sum(
                s['success_rate_percent'] * s['total_requests'] 
                for s in stats.values()
            ) / max(1, sum(s['total_requests'] for s in stats.values())),
            'clients': stats
        }

# Globale HTTP Client Pool Manager Instanz
_http_pool_manager: Optional[HttpClientPoolManager] = None

async def get_http_client_pool() -> HttpClientPoolManager:
    """
    Singleton-Pattern für globalen HTTP Client Pool Manager
    
    Returns:
        HttpClientPoolManager Instanz
    """
    global _http_pool_manager
    if _http_pool_manager is None:
        _http_pool_manager = HttpClientPoolManager()
    return _http_pool_manager

@asynccontextmanager
async def get_api_client(provider: str, base_url: str, headers: Optional[Dict[str, str]] = None):
    """
    Context Manager für API HTTP Client
    
    Args:
        provider: API Provider Name
        base_url: API Base URL
        headers: Optional HTTP Headers
        
    Yields:
        SharedHttpClient für API Provider
        
    Example:
        async with get_api_client('alpha_vantage', 'https://www.alphavantage.co/') as client:
            response = await client.get_json('query?function=TIME_SERIES_DAILY&symbol=IBM')
    """
    pool_manager = await get_http_client_pool()
    client = await pool_manager.get_api_client(provider, base_url, headers)
    try:
        yield client
    finally:
        # Client wird nicht geschlossen - Connection Pool bleibt aktiv
        pass

