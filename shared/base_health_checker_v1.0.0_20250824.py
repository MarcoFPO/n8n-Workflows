class HttpEndpointCheck(IHealthCheck):
    """HTTP Endpoint Health Check"""
    
    def __init__(self, name: str, url: str, timeout: float = 5.0, 
                 expected_status: int = 200, headers: Optional[Dict[str, str]] = None):
        self._name = name
        self.url = url
        self.timeout = timeout
        self.expected_status = expected_status
        self.headers = headers or {}
    
    @property
    def name(self) -> str:
        return self._name
    
    @property
    def check_type(self) -> CheckType:
        return CheckType.HTTP_ENDPOINT
    
    async def check(self) -> HealthCheckResult:
        start_time = time.time()
        
        try:
            timeout = aiohttp.ClientTimeout(total=self.timeout)
            async with aiohttp.ClientSession(timeout=timeout) as session:
                async with session.get(self.url, headers=self.headers) as response:
                    duration_ms = (time.time() - start_time) * 1000
                    
                    if response.status == self.expected_status:
                        return HealthCheckResult(
                            check_name=self.name,
                            status=HealthStatus.HEALTHY,
                            message=f"HTTP endpoint responding (status: {response.status})",
                            details={
                                "url": self.url,
                                "status_code": response.status,
                                "response_time_ms": duration_ms
                            },
                            duration_ms=duration_ms
                        )
                    else:
                        return HealthCheckResult(
                            check_name=self.name,
                            status=HealthStatus.DEGRADED,
                            message=f"HTTP endpoint returned unexpected status: {response.status}",
                            details={
                                "url": self.url,
                                "status_code": response.status,
                                "expected_status": self.expected_status,
                                "response_time_ms": duration_ms
                            },
                            duration_ms=duration_ms,
                            severity=CheckSeverity.MEDIUM
                        )
                        
        except asyncio.TimeoutError:
            duration_ms = (time.time() - start_time) * 1000
            return HealthCheckResult(
                check_name=self.name,
                status=HealthStatus.UNHEALTHY,
                message=f"HTTP endpoint timeout after {self.timeout}s",
                details={"url": self.url, "timeout": self.timeout},
                duration_ms=duration_ms,
                severity=CheckSeverity.HIGH,
                error="Timeout"
            )
        except Exception as e:
            duration_ms = (time.time() - start_time) * 1000
            return HealthCheckResult(
                check_name=self.name,
                status=HealthStatus.UNHEALTHY,
                message=f"HTTP endpoint check failed: {str(e)}",
                details={"url": self.url, "error": str(e)},
                duration_ms=duration_ms,
                severity=CheckSeverity.HIGH,
                error=str(e)
            )

