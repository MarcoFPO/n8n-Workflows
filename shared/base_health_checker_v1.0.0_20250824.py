# HEALTH CHECK TYPES AND ENUMS

class HealthStatus(Enum):
    """Health Status Levels"""
    HEALTHY = "healthy"
    DEGRADED = "degraded" 
    UNHEALTHY = "unhealthy"
    UNKNOWN = "unknown"
    CRITICAL = "critical"

class CheckType(Enum):
    """Health Check Types"""
    HTTP_ENDPOINT = "http_endpoint"
    DATABASE_CONNECTION = "database_connection"
    REDIS_CONNECTION = "redis_connection"
    FILE_SYSTEM = "file_system"
    MEMORY_USAGE = "memory_usage"
    CPU_USAGE = "cpu_usage"
    DISK_USAGE = "disk_usage"
    NETWORK_CONNECTIVITY = "network_connectivity"
    SERVICE_DEPENDENCY = "service_dependency"
    CUSTOM = "custom"

class CheckSeverity(Enum):
    """Health Check Severity Levels"""
    LOW = 1
    MEDIUM = 2
    HIGH = 3
    CRITICAL = 4

@dataclass
class HealthCheckResult:
    """Health Check Result"""
    check_name: str
    status: HealthStatus
    message: str
    details: Dict[str, Any] = field(default_factory=dict)
    duration_ms: float = 0.0
    timestamp: datetime = field(default_factory=datetime.now)
    severity: CheckSeverity = CheckSeverity.MEDIUM
    error: Optional[str] = None
    
    def to_dict(self) -> Dict[str, Any]:
        """Convert to dictionary"""
        return {
            "check_name": self.check_name,
            "status": self.status.value,
            "message": self.message,
            "details": self.details,
            "duration_ms": self.duration_ms,
            "timestamp": self.timestamp.isoformat(),
            "severity": self.severity.value,
            "error": self.error
        }
    
    @property
    def is_healthy(self) -> bool:
        return self.status == HealthStatus.HEALTHY
    
    @property
    def is_critical(self) -> bool:
        return self.status == HealthStatus.CRITICAL or self.severity == CheckSeverity.CRITICAL

@dataclass
class HealthSummary:
    """Overall Health Summary"""
    overall_status: HealthStatus
    total_checks: int
    healthy_checks: int
    degraded_checks: int
    unhealthy_checks: int
    critical_checks: int
    check_results: List[HealthCheckResult] = field(default_factory=list)
    summary_message: str = ""
    timestamp: datetime = field(default_factory=datetime.now)
    
    @property
    def health_percentage(self) -> float:
        if self.total_checks == 0:
            return 0.0
        return (self.healthy_checks / self.total_checks) * 100.0
    
    def to_dict(self) -> Dict[str, Any]:
        """Convert to dictionary"""
        return {
            "overall_status": self.overall_status.value,
            "health_percentage": self.health_percentage,
            "total_checks": self.total_checks,
            "healthy_checks": self.healthy_checks,
            "degraded_checks": self.degraded_checks,
            "unhealthy_checks": self.unhealthy_checks,
            "critical_checks": self.critical_checks,
            "summary_message": self.summary_message,
            "timestamp": self.timestamp.isoformat(),
            "check_results": [result.to_dict() for result in self.check_results]
        }

# HEALTH CHECK INTERFACES - Interface Segregation Principle

class IHealthCheck(ABC):
    """Health Check Interface"""
    
    @abstractmethod
    async def check(self) -> HealthCheckResult:
        """Perform health check"""
        pass
    
    @property
    @abstractmethod
    def name(self) -> str:
        """Check name"""
        pass
    
    @property
    @abstractmethod
    def check_type(self) -> CheckType:
        """Check type"""
        pass

class IHealthAggregator(ABC):
    """Health Check Aggregator Interface"""
    
    @abstractmethod
    async def aggregate_health(self, results: List[HealthCheckResult]) -> HealthSummary:
        """Aggregate health check results"""
        pass

class IHealthReporter(ABC):
    """Health Check Reporter Interface"""
    
    @abstractmethod
    async def report_health(self, summary: HealthSummary) -> bool:
        """Report health status"""
        pass

