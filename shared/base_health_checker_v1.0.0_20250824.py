class CustomFunctionCheck(IHealthCheck):
    """Custom Function Health Check"""
    
    def __init__(self, name: str, check_func: Callable[[], Any], 
                 timeout: float = 10.0, severity: CheckSeverity = CheckSeverity.MEDIUM):
        self._name = name
        self.check_func = check_func
        self.timeout = timeout
        self.severity = severity
    
    @property
    def name(self) -> str:
        return self._name
    
    @property
    def check_type(self) -> CheckType:
        return CheckType.CUSTOM
    
    async def check(self) -> HealthCheckResult:
        start_time = time.time()
        
        try:
            # Execute custom function with timeout
            if asyncio.iscoroutinefunction(self.check_func):
                result = await asyncio.wait_for(
                    self.check_func(), 
                    timeout=self.timeout
                )
            else:
                result = await asyncio.wait_for(
                    asyncio.to_thread(self.check_func),
                    timeout=self.timeout
                )
            
            duration_ms = (time.time() - start_time) * 1000
            
            # Interpret result based on type
            if isinstance(result, bool):
                if result:
                    return HealthCheckResult(
                        check_name=self.name,
                        status=HealthStatus.HEALTHY,
                        message="Custom check passed",
                        details={"result": result},
                        duration_ms=duration_ms
                    )
                else:
                    return HealthCheckResult(
                        check_name=self.name,
                        status=HealthStatus.UNHEALTHY,
                        message="Custom check failed",
                        details={"result": result},
                        duration_ms=duration_ms,
                        severity=self.severity
                    )
            elif isinstance(result, dict):
                # Expect dict with status and message
                status_str = result.get('status', 'unknown')
                try:
                    status = HealthStatus(status_str)
                except ValueError:
                    status = HealthStatus.UNKNOWN
                
                return HealthCheckResult(
                    check_name=self.name,
                    status=status,
                    message=result.get('message', 'Custom check completed'),
                    details=result.get('details', {}),
                    duration_ms=duration_ms,
                    severity=self.severity
                )
            else:
                # Any other result type considered healthy if not None
                return HealthCheckResult(
                    check_name=self.name,
                    status=HealthStatus.HEALTHY if result is not None else HealthStatus.UNHEALTHY,
                    message=f"Custom check returned: {type(result).__name__}",
                    details={"result": str(result)},
                    duration_ms=duration_ms
                )
                
        except asyncio.TimeoutError:
            duration_ms = (time.time() - start_time) * 1000
            return HealthCheckResult(
                check_name=self.name,
                status=HealthStatus.UNHEALTHY,
                message=f"Custom check timeout after {self.timeout}s",
                details={"timeout": self.timeout},
                duration_ms=duration_ms,
                severity=CheckSeverity.HIGH,
                error="Timeout"
            )
        except Exception as e:
            duration_ms = (time.time() - start_time) * 1000
            return HealthCheckResult(
                check_name=self.name,
                status=HealthStatus.UNHEALTHY,
                message=f"Custom check failed: {str(e)}",
                details={"error": str(e)},
                duration_ms=duration_ms,
                severity=self.severity,
                error=str(e)
            )

# HEALTH CHECK AGGREGATOR

class StandardHealthAggregator(IHealthAggregator):
    """Standard Health Check Aggregator"""
    
    async def aggregate_health(self, results: List[HealthCheckResult]) -> HealthSummary:
        """Aggregate health check results into summary"""
        if not results:
            return HealthSummary(
                overall_status=HealthStatus.UNKNOWN,
                total_checks=0,
                healthy_checks=0,
                degraded_checks=0,
                unhealthy_checks=0,
                critical_checks=0,
                summary_message="No health checks configured"
            )
        
        # Count statuses
        status_counts = {
            HealthStatus.HEALTHY: 0,
            HealthStatus.DEGRADED: 0,
            HealthStatus.UNHEALTHY: 0,
            HealthStatus.CRITICAL: 0,
            HealthStatus.UNKNOWN: 0
        }
        
        for result in results:
            status_counts[result.status] += 1
        
        # Determine overall status
        if status_counts[HealthStatus.CRITICAL] > 0:
            overall_status = HealthStatus.CRITICAL
            summary_message = f"{status_counts[HealthStatus.CRITICAL]} critical issue(s) detected"
        elif status_counts[HealthStatus.UNHEALTHY] > 0:
            overall_status = HealthStatus.UNHEALTHY
            summary_message = f"{status_counts[HealthStatus.UNHEALTHY]} unhealthy check(s)"
        elif status_counts[HealthStatus.DEGRADED] > 0:
            overall_status = HealthStatus.DEGRADED
            summary_message = f"{status_counts[HealthStatus.DEGRADED]} degraded check(s)"
        elif status_counts[HealthStatus.UNKNOWN] == len(results):
            overall_status = HealthStatus.UNKNOWN
            summary_message = "All checks returned unknown status"
        else:
            overall_status = HealthStatus.HEALTHY
            summary_message = "All systems operational"
        
        return HealthSummary(
            overall_status=overall_status,
            total_checks=len(results),
            healthy_checks=status_counts[HealthStatus.HEALTHY],
            degraded_checks=status_counts[HealthStatus.DEGRADED],
            unhealthy_checks=status_counts[HealthStatus.UNHEALTHY],
            critical_checks=status_counts[HealthStatus.CRITICAL],
            check_results=results,
            summary_message=summary_message
        )

