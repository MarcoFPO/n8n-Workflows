class MemoryUsageCheck(IHealthCheck):
    """Memory Usage Health Check"""
    
    def __init__(self, name: str, warning_threshold: float = 80.0, critical_threshold: float = 95.0):
        self._name = name
        self.warning_threshold = warning_threshold
        self.critical_threshold = critical_threshold
    
    @property
    def name(self) -> str:
        return self._name
    
    @property
    def check_type(self) -> CheckType:
        return CheckType.MEMORY_USAGE
    
    async def check(self) -> HealthCheckResult:
        start_time = time.time()
        
        try:
            memory = psutil.virtual_memory()
            usage_percent = memory.percent
            
            duration_ms = (time.time() - start_time) * 1000
            
            details = {
                "usage_percent": usage_percent,
                "total_gb": round(memory.total / (1024**3), 2),
                "available_gb": round(memory.available / (1024**3), 2),
                "used_gb": round(memory.used / (1024**3), 2),
                "warning_threshold": self.warning_threshold,
                "critical_threshold": self.critical_threshold
            }
            
            if usage_percent >= self.critical_threshold:
                return HealthCheckResult(
                    check_name=self.name,
                    status=HealthStatus.CRITICAL,
                    message=f"Memory usage critical: {usage_percent:.1f}%",
                    details=details,
                    duration_ms=duration_ms,
                    severity=CheckSeverity.CRITICAL
                )
            elif usage_percent >= self.warning_threshold:
                return HealthCheckResult(
                    check_name=self.name,
                    status=HealthStatus.DEGRADED,
                    message=f"Memory usage high: {usage_percent:.1f}%",
                    details=details,
                    duration_ms=duration_ms,
                    severity=CheckSeverity.HIGH
                )
            else:
                return HealthCheckResult(
                    check_name=self.name,
                    status=HealthStatus.HEALTHY,
                    message=f"Memory usage normal: {usage_percent:.1f}%",
                    details=details,
                    duration_ms=duration_ms
                )
                
        except Exception as e:
            duration_ms = (time.time() - start_time) * 1000
            return HealthCheckResult(
                check_name=self.name,
                status=HealthStatus.UNKNOWN,
                message=f"Memory check failed: {str(e)}",
                details={"error": str(e)},
                duration_ms=duration_ms,
                error=str(e)
            )

class CpuUsageCheck(IHealthCheck):
    """CPU Usage Health Check"""
    
    def __init__(self, name: str, warning_threshold: float = 80.0, 
                 critical_threshold: float = 95.0, interval: float = 1.0):
        self._name = name
        self.warning_threshold = warning_threshold
        self.critical_threshold = critical_threshold
